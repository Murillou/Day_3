const now = new Date()
console.log(now.getFullYear()) // ANO
console.log(now.getMonth()) // MES
console.log(now.getDate()) // DATA
console.log(now.getDay()) // DIA 0-6 
console.log(now.getHours() + ':' + now.getMinutes() + '.' + now.getSeconds())
console.log(now.getMinutes()) // MINUTOS
console.log(now.getSeconds()) //SEGUNDOS
console.log(now.getTime()); // MILLISEGUNDOS DESDE JANEIRO DE 1970
console.log(Date.now()) // MESMO DE CIMA APENAS DE OUTRA FORMA
